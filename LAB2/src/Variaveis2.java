public class Variaveis2 {
    public static void main(String[] args) {
        int variavel = 5;
        System.out.println(variavel);
        variavel = 50;
        System.out.println(variavel);
        int outraVariavel = 100;
        System.out.println(outraVariavel);
        variavel = outraVariavel;
        outraVariavel = 200;
        System.out.println(variavel);
        System.out.println(outraVariavel);

    }    
}
