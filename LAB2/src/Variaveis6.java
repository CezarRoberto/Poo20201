public class Variaveis6 {
    public static void main(String[] args) {
        // operadores de atribuicao composta
        int valor = 50;
        valor -= 5;
        System.out.println(valor); //45

        int resultado = 100;
        System.out.println(resultado);

        int valor1 = 5;
        int valor2 = 10;
        resultado /= valor1 * valor2; // 5 * 10 = 50 -> 100 / 50 = 2
        System.out.println(resultado);
        
    }
}
